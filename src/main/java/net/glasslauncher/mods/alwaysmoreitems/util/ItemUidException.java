package net.glasslauncher.mods.alwaysmoreitems.util;

public class ItemUidException extends RuntimeException {
	/**
	 * Constructs a {@code ItemUidException} with no detail message.
	 */
	public ItemUidException() {
		super();
	}

	/**
	 * Constructs a {@code ItemUidException} with the specified
	 * detail message.
	 *
	 * @param   s   the detail message.
	 */
	public ItemUidException(String s) {
		super(s);
	}
}
